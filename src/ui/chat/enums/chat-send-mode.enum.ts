/**
 * Chat send modes enum
 *
 * This enum represents the modes values in which the chat is being sent.
 *
 * @property image - The mode in which the chat is being sent as an image
 * @property internal - The mode in which the chat is being sent as an internal message
 * @property text - The mode in which the chat is being sent as a text message
 */
export enum ChatSendModes {
  IMAGE = 'image',
  INTERNAL = 'internal',
  TEXT = 'text',
}
