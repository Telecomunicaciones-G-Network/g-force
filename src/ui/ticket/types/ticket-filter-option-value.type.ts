/**
 * @name TicketFilterOptionValue
 *
 * @description This type represents the possible values for filtering tickets.
 *
 * @property Abierto - The ticket is open
 * @property Cerrado - The ticket is closed
 * @property Todos - The ticket is all
 */
export type TicketFilterOptionValue = 'Abierto' | 'Cerrado' | 'Todos';
