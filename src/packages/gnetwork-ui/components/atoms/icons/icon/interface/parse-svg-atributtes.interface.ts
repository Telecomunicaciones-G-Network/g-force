/**
 * Parse SVG attributes interface.
 *
 * @param fill - The fill.
 * @param innerHTML - The inner HTML.
 * @param stroke - The stroke.
 * @param strokeLinecap - The stroke linecap.
 * @param strokeLinejoin - The stroke linejoin.
 * @param strokeWidth - The stroke width.
 * @param viewBox - The view box.
 */
export interface ParseSVGAttributesInterface {
  fill: string;
  innerHTML: string;
  stroke: string;
  strokeLinecap: string;
  strokeLinejoin: string;
  strokeWidth: string;
  viewBox: string;
}
