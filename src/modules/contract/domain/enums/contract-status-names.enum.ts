/**
 * @name ContractStatusNames
 *
 * @description Representa los posibles nombres de estado de un contrato en el sistema.
 *
 * @property ACTIVE - El contrato está activo
 * @property CANCELLED - El contrato ha sido cancelado
 * @property SUSPENDED - El contrato ha sido suspendido
 */
export enum ContractStatusNames {
  ACTIVE = 'Activo',
  CANCELLED = 'Cancelado',
  SUSPENDED = 'Suspendido',
}
