/**
 * @name ContractStatusName
 *
 * @description Este tipo representa los posibles nombres de estado de un contrato en el sistema.
 *
 * @property Activo - El contrato está activo
 * @property Cancelado - El contrato ha sido cancelado
 * @property Suspendido - El contrato ha sido suspendido
 */
export type ContractStatusName = 'Activo' | 'Cancelado' | 'Suspendido';
