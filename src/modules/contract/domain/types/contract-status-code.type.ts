/**
 * @name ContractStatusCode
 *
 * @description This type represents the possible statuses of a contract within the system.
 *
 * @property ACTIVE - The contract is active
 * @property CANCELLED - The contract has been cancelled
 * @property SUSPENDED - The contract has been suspended
 */
export type ContractStatusCode = 'ACTIVE' | 'CANCELLED' | 'SUSPENDED';
