/**
 * @name MediaType
 *
 * @description This type represents the possible types of media within the system.
 *
 * @property AUDIO - The media is an audio
 * @property DOCUMENT - The media is a document
 * @property IMAGE - The media is an image
 * @property STICKER - The media is a sticker
 * @property VIDEO - The media is a video
 */
export type MediaType = 'AUDIO' | 'DOCUMENT' | 'IMAGE' | 'STICKER' | 'VIDEO';
