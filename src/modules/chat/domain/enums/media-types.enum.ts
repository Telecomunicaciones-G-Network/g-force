/**
 * @name MediaTypes
 *
 * @description Represents the possible types of media within the system.
 *
 * @property AUDIO - The media is an audio file
 * @property DOCUMENT - The media is a document file
 * @property IMAGE - The media is an image file
 * @property STICKER - The media is a sticker
 * @property VIDEO - The media is a video file
 */
export enum MediaTypes {
  AUDIO = 'AUDIO',
  DOCUMENT = 'DOCUMENT',
  IMAGE = 'IMAGE',
  STICKER = 'STICKER',
  VIDEO = 'VIDEO',
}
