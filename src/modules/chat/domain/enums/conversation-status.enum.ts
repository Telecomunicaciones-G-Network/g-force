/**
 * Conversation status enum
 *
 * @property ACTIVE - The conversation is active
 * @property ASSIGNED - The conversation is assigned
 * @property FINISHED - The conversation is finished
 * @property WAITING - The conversation is waiting
 */
export enum ConversationStatus {
  ACTIVE = 'ACTIVE',
  ASSIGNED = 'ASSIGNED',
  FINISHED = 'FINISHED',
  WAITING = 'WAITING',
}
